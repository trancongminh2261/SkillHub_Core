        [HttpPost]
        [Route("upload")]
        [ClaimsAuthorize]
        public IActionResult Upload()
        {
            try
            {
                var file = HttpContext.Request.Form.Files.GetFile("File");
                string baseUrl = $"{Request.Scheme}://{Request.Host.Value}";
                var upload = UploadConfig.UploadFile(file, baseUrl, "Upload/FileIeltsExam/");
                if (upload.Success)
                {
                    return Ok(new { data = upload.Link, message = upload.Message });
                }
                else
                    return BadRequest(new { message = upload.Message });
            }
            catch (Exception ex)
            {
                return BadRequest(new { message = ex.Message });
            }
        }
